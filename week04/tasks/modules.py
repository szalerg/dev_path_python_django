import math
import random

# Korzystanie z math
# Napisz funkcję, która oblicza pierwiastek kwadratowy z liczby (użyj math.sqrt).
def square_root(x):

    return math.sqrt(x)

# Losowa liczba
# Napisz funkcję, która zwraca losową liczbę od 1 do 10 (użyj modułu random).

def rng(x, y):
    return random.randint(x, y)
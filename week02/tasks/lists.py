"""
Zadania związane z listami.
"""

def zadanie_1(lst):
    # ⭐ Napisz funkcję, która zwraca sumę elementów listy
    return sum(lst)

def zadanie_2(lst):
    # ⭐⭐ Napisz funkcję, która odwraca listę
    return lst[::-1]    

def zadanie_3(lst):
    # ⭐⭐⭐ Napisz funkcję, która zwraca największy element i jego indeks
    
    return max(lst)
